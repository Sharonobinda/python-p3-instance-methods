#!/usr/bin/env python3

class Dog:
    # Instance Method Definition
    def bark(self):
        print("Woof!")
    
    # Adding the sit method with the correct message
    def sit(self):
        print("The dog is sitting.")

class Person:
    # Instance method definition to introduce themselves
    def introduce(self):
        print("Hello, my name is John Doe.")
    
    # Adding the talk method
    def talk(self):
        print("Hello World!")
    
    # Adding the walk method
    def walk(self):
        print("The person is walking.")